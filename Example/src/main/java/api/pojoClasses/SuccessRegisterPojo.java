package api.pojoClasses;

public class SuccessRegisterPojo extends MainPojo {
    private Integer id;
    private String token;

    public SuccessRegisterPojo() {
        this.id = id;
        this.token = token;
    }

    public Integer getId() {
        return id;
    }

    public String getToken() {
        return token;
    }

}